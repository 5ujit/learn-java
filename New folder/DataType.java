public class DataType {
    public static void main(String[] args) {
        int num = 1;
        byte by = 23;

        
        short sh = 29333;
        long l = 299999999;

        float f = 5.2f;
        double d = 5.555555555900000080;

        char c = 'k';
        boolean b = true;

        // Print the variables using System.out.println()
        System.out.println("The value of num (int) is: " + num);
        System.out.println("The value of by (byte) is: " + by);
        System.out.println("The value of sh (short) is: " + sh);
        System.out.println("The value of l (long) is: " + l);
        System.out.println("The value of f (float) is: " + f);
        System.out.println("The value of d (double) is: " + d);
        System.out.println("The value of c (char) is: " + c);
        System.out.println("The value of b (boolean) is: " + b);
    }
}
